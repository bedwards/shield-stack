-- state_probate_rules: public read-only data
ALTER TABLE state_probate_rules ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Anyone can read state probate rules"
  ON state_probate_rules FOR SELECT
  TO anon, authenticated
  USING (true);

