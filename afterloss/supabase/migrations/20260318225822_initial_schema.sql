-- cases: authenticated users manage their own cases
ALTER TABLE cases ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can read their own cases"
  ON cases FOR SELECT
  TO authenticated
  USING (auth.uid() = user_id);

CREATE POLICY "Users can create cases"
  ON cases FOR INSERT
  TO authenticated
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update their own cases"
  ON cases FOR UPDATE
  TO authenticated
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can delete their own cases"
  ON cases FOR DELETE
  TO authenticated
  USING (auth.uid() = user_id);

-- case_checklist_items: access via case ownership
ALTER TABLE case_checklist_items ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can read their own checklist items"
  ON case_checklist_items FOR SELECT
  TO authenticated
  USING (EXISTS (
    SELECT 1 FROM cases WHERE cases.id = case_checklist_items.case_id AND cases.user_id = auth.uid()
  ));

CREATE POLICY "Users can create checklist items for their cases"
  ON case_checklist_items FOR INSERT
  TO authenticated
  WITH CHECK (EXISTS (
    SELECT 1 FROM cases WHERE cases.id = case_checklist_items.case_id AND cases.user_id = auth.uid()
  ));

CREATE POLICY "Users can update their own checklist items"
  ON case_checklist_items FOR UPDATE
  TO authenticated
  USING (EXISTS (
    SELECT 1 FROM cases WHERE cases.id = case_checklist_items.case_id AND cases.user_id = auth.uid()
  ))
  WITH CHECK (EXISTS (
    SELECT 1 FROM cases WHERE cases.id = case_checklist_items.case_id AND cases.user_id = auth.uid()
  ));

CREATE POLICY "Users can delete their own checklist items"
